/**
 * @param {Record<string, unknown>} item Catalog YAML entry (English source fields).
 * @returns {{ fr: Record<string, unknown>, de: Record<string, unknown>, ar: Record<string, unknown> }}
 */
export function buildCatalogTranslations(item) {
  const slug = typeof item.slug === 'string' ? item.slug : '';
  const entry = translationsBySlug[slug];

  if (!entry) {
    return { fr: {}, de: {}, ar: {} };
  }

  return {
    fr: pickLocaleCopy(item, entry.fr),
    de: pickLocaleCopy(item, entry.de),
    ar: pickLocaleCopy(item, entry.ar),
  };
}
