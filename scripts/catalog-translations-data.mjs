const FIELDS_BY_KIND = {
  destinations: ['name', 'country', 'description', 'priceFrom', 'duration', 'highlights'],
  hotels: ['name', 'country', 'description', 'priceFrom', 'city', 'highlights', 'amenities'],
  flights: ['name', 'country', 'description', 'priceFrom', 'duration', 'route', 'airline', 'highlights'],
};

function inferKind(item) {
  if (item.route != null) return 'flights';
  if (item.city != null) return 'hotels';
  return 'destinations';
}

function pickLocaleCopy(item, localeCopy) {
  const kind = inferKind(item);
  const allowed = FIELDS_BY_KIND[kind];
  const copy = {};

  for (const field of allowed) {
    if (item[field] == null || localeCopy[field] == null) continue;
    copy[field] = localeCopy[field];
  }

  return copy;
}

